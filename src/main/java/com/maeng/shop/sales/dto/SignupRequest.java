package com.maeng.shop.sales.dto;

public class SignupRequest {
    private String email;

    private String password;

    private int age;

    private String name;

    public String getEmail() {
        return email;
    }

    public String getPassword() {
        return password;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }
}
